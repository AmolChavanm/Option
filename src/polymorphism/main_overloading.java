package polymorphism;

public class main_overloading 
{
	public static void main(String[] args)
	{
		
		System.out.println("this is main method with string args");
		
	}

	public static void main(int a) 
	{
		System.out.println("Thsi is main method with int argument");
	}

	public static void main(float a)
	{
		System.out.println("Thsi is main method with the float argument");
		
	}
}
